    private void getMahasiswa() {
        DefaultTableModel model = (DefaultTableModel) tbl_mahasiswa.getModel();
        model.setRowCount(0);
        
        try {
            String q = "SELECT * FROM mahasiswa";
            PreparedStatement st = conn.prepareStatement(q);
            ResultSet rs = st.executeQuery();
            while(rs.next()) {
                int id = rs.getInt("id");
                String nama = rs.getString("nama");
                String nim = rs.getString("nim");
                
                Object[] dataMahasiswa = {id,nama,nim};
                
                model.addRow(dataMahasiswa);
            }
            
            rs.close();
            st.close();
        } catch (Exception e) {
            Logger.getLogger(MahasiswaFrame.class.getName()).log(Level.SEVERE, null, e);
        }
    }

