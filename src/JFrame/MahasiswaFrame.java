    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int isRowSelected = tbl_mahasiswa.getSelectedRow();
        
        if (isRowSelected == -1) {
            JOptionPane.showMessageDialog(this, "Pilih salah satu data yg ingin dihapus");
            return;
        }
        
        int decision = JOptionPane.showConfirmDialog(this, "yakin mau diapus?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        
        if (decision == JOptionPane.YES_OPTION) {
            String id = tbl_mahasiswa.getValueAt(isRowSelected, 0).toString();
            
            try {
            String q = "DELETE FROM mahasiswa WHERE id=?";
            PreparedStatement st = conn.prepareStatement(q);
            st.setString(1, id);
            
            int rowDeleted = st.executeUpdate();
            
            if (rowDeleted > 0){
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                resetForm();
                getMahasiswa();
            }
            st.close();
            } catch (Exception e) {
                Logger.getLogger(MahasiswaFrame.class.getName()).log(Level.SEVERE, null, e);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

