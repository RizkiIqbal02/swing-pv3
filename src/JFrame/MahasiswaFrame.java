    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        String nama = tFNama.getText();
        String nim = tFNIM.getText();
        
        if (nama.isEmpty() || nim.isEmpty()){
            JOptionPane.showMessageDialog(this, "Data kosong, harap isi");
            return;
        }
        
        try {
            String q = "INSERT INTO mahasiswa (nama, nim) VALUES (?,?)";
            PreparedStatement st = conn.prepareStatement(q);
            st.setString(1, nama);
            st.setString(2, nim);
            
            int rowInserted = st.executeUpdate();
            
            if (rowInserted > 0){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan");
                resetForm();
                getMahasiswa();
            }
            st.close();
        } catch (Exception e) {
            Logger.getLogger(MahasiswaFrame.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int isRowSelected = tbl_mahasiswa.getSelectedRow();
        
        if (isRowSelected == -1) {
            JOptionPane.showMessageDialog(this, "Pilih salah satu data yg ingin diubah");
            return;
        }
        
        String id = tbl_mahasiswa.getValueAt(isRowSelected, 0).toString();
        String nama = tFNama.getText();
        String nim = tFNIM.getText();
        
        if (nama.isEmpty() || nim.isEmpty()){
            JOptionPane.showMessageDialog(this, "Data kosong, harap isi");
            return;
        }

        try {
            String q = "UPDATE mahasiswa SET nama=?, nim=? WHERE id=?";
            PreparedStatement st = conn.prepareStatement(q);
            st.setString(1, nama);
            st.setString(2, nim);
            st.setString(3, id);
            
            int rowUpdated = st.executeUpdate();
            
            if (rowUpdated > 0){
                JOptionPane.showMessageDialog(this, "Data berhasil diperbarui");
                resetForm();
                getMahasiswa();
            }
            st.close();
        } catch (Exception e) {
            Logger.getLogger(MahasiswaFrame.class.getName()).log(Level.SEVERE, null, e);
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

